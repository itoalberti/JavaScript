const day = 'friday';

switch (day) {
    case 'monday':
        console.log('The work week has just started!');
        break;
    case 'tuesday':
        break;
    case 'wednesday':
        console.log('It is wednesday my dudes');
        break;
    case 'thursday':
        console.log('It\'s almost the weekend');
        break;
    case 'friday':
        console.log('I declare open the weekend.\nPlease bring me an ice cold Glacial and a red Derby');
        break;
    case 'saturday':
        console.log('Weekend is at its peak!');
        break;
    case 'sunday':
        console.log('Church day');
        break;
    default:
        console.log('Not a valid day!');
}